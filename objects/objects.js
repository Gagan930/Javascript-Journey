// In javascript objects are used to store key value pairs

let Student = {
    name :"Gagan",
    age : 21,
    color : "Brown"
};

// we can console  the object attributes

console.log(Student.name);
console.log(Student.age);


// we can also update the attributes 

Student.age=22;
Student.color="White";
console.log(Student.name);
console.log(Student.age);

// we can also print whole object 
console.log(Student);

